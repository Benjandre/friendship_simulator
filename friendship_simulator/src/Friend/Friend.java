package Friend;

public class Friend {
    
    String friendName;
    int friendAge;

    public Friend(String friendName, int friendAge) {
        this.friendName = friendName;
        this.friendAge = friendAge;
    }

    public String getFriendName() {
        return friendName;
    }

    public int getFriendAge() {
        return friendAge;
    }
    
}
