package Friend;

public class Friend {
    
    String fristName;
    String lastName;
    int friendAge;

    public Friend(String fristName, String lastName, int friendAge) {
        this.fristName = fristName;
        this.lastName = lastName;
        this.friendAge = friendAge;
    }

    public String getFriendName() {
        return friendName;
    }

    public int getFriendAge() {
        return friendAge;
    }
    
}
